export type SortKey = 'price' | 'days' | 'goDate' | 'transferCount'

export function formatPrice(n: number) {
  return `NT$${n.toLocaleString('zh-TW')}`
}

export function sortTours(tours: EgyptTour[], key: SortKey, dir: 'asc' | 'desc' = 'asc') {
  const mul = dir === 'asc' ? 1 : -1
  return [...tours].sort((a, b) => {
    if (key === 'goDate') return a.goDate.localeCompare(b.goDate) * mul
    return (a[key] - b[key]) * mul
  })
}
